<?php

namespace App\Entity\Main;

use App\Entity\Server\players;
use App\Repository\Main\FTopicRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=FTopicRepository::class)
 */
class FTopic
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $title;

    /**
     * @ORM\Column(type="datetime")
     */
    private $create_at;

    /**
     * @ORM\Column(type="boolean")
     */
    private $resolved;

    /**
     * @ORM\ManyToOne(targetEntity=FSubcategory::class, inversedBy="topics")
     */
    private $subcategory;

    /**
     * @ORM\Column(type="text")
     */
    private $content;

    /**
     * @ORM\Column(type="boolean")
     */
    private $owner_notif;

    /**
     * @ORM\ManyToOne(targetEntity=Users::class, inversedBy="topics")
     */
    private $owner;

    /**
     * @ORM\OneToMany(targetEntity=FMessage::class, mappedBy="topic")
     */
    private $messages;

    public function __construct()
    {
        $this->messages = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }

    public function getCreateAt(): ?\DateTimeInterface
    {
        return $this->create_at;
    }

    public function setCreateAt(\DateTimeInterface $create_at): self
    {
        $this->create_at = $create_at;

        return $this;
    }

    public function getResolved(): ?bool
    {
        return $this->resolved;
    }

    public function setResolved(bool $resolved): self
    {
        $this->resolved = $resolved;

        return $this;
    }

    public function getSubcategory(): ?FSubcategory
    {
        return $this->subcategory;
    }

    public function setSubcategory(?FSubcategory $subcategory): self
    {
        $this->subcategory = $subcategory;

        return $this;
    }

    public function getContent(): ?string
    {
        return $this->content;
    }

    public function setContent(string $content): self
    {
        $this->content = $content;

        return $this;
    }

    public function getOwnerNotif(): ?bool
    {
        return $this->owner_notif;
    }

    public function setOwnerNotif(bool $owner_notif): self
    {
        $this->owner_notif = $owner_notif;

        return $this;
    }

    public function getOwner(): ?Users
    {
        return $this->owner;
    }

    public function setOwner(?Users $owner): self
    {
        $this->owner = $owner;

        return $this;
    }

